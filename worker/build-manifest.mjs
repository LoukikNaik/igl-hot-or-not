// Regenerate src/photos.js — the list of person ids that have a public/photos/<id>.jpg.
// Workers have no filesystem, so the roster's "does a photo exist" check is baked at build.
// Run after adding/removing photos:  node worker/build-manifest.mjs
import { readdirSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const photosDir = join(here, '..', 'public', 'photos');
const ids = readdirSync(photosDir).filter(f => f.endsWith('.jpg')).map(f => f.slice(0, -4)).sort();
writeFileSync(join(here, 'src', 'photos.js'),
  `// generated by worker/build-manifest.mjs — ids that have public/photos/<id>.jpg\nexport default ${JSON.stringify(ids)};\n`);
console.log('photos.js:', ids.length, 'ids');
